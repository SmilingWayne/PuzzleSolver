import pytest
from puzzlekit.core.grid import Grid
from puzzlekit.solvers.akari import AkariSolver 
class TestData:
    pass

@pytest.fixture
def data():
    d = TestData()
    d.puzzle_dict = {
        "num_rows": 10, 
        "num_cols": 10, 
        "grid": [['1', '-', '-', '-', '-', '-', '-', '-', '-', '1'],
                ['-', '-', '-', 'x', '-', '-', '-', '-', '-', '-'],
                ['-', 'x', '-', '-', '-', '2', '-', '-', 'x', '-'],
                ['-', '-', '-', '-', '-', '-', '-', '1', '-', '-'],
                ['-', '-', '-', '4', '-', '-', '-', '-', '-', '-'],
                ['-', '-', '-', '-', '-', '-', '2', '-', '-', '-'],
                ['-', '-', '2', '-', '-', '-', '-', '-', '-', '-'],
                ['-', 'x', '-', '-', '2', '-', '-', '-', 'x', '-'],
                ['-', '-', '-', '-', '-', '-', '0', '-', '-', '-'],
                ['1', '-', '-', '-', '-', '-', '-', '-', '-', '1']]
        }
    return d

def test_akari(data):
    exp_grid = [['1', '-', '-', '-', '-', '-', '-', '-', 'o', '1'],
                ['o', '-', '-', 'x', '-', 'o', '-', '-', '-', '-'],
                ['-', 'x', '-', '-', '-', '2', 'o', '-', 'x', '-'],
                ['-', '-', '-', 'o', '-', '-', '-', '1', 'o', '-'],
                ['-', '-', 'o', '4', 'o', '-', '-', '-', '-', '-'],
                ['-', '-', '-', 'o', '-', '-', '2', 'o', '-', '-'],
                ['-', 'o', '2', '-', '-', '-', 'o', '-', '-', '-'],
                ['-', 'x', 'o', '-', '2', 'o', '-', '-', 'x', '-'],
                ['-', '-', '-', '-', 'o', '-', '0', '-', '-', 'o'],
                ['1', 'o', '-', '-', '-', '-', '-', '-', '-', '1']]
    solver = AkariSolver(**data.puzzle_dict)
    res_grid = solver.solve_and_show(show = True).get('grid', [])
    assert Grid(exp_grid) == res_grid
    